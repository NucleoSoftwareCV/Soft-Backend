    inicio_at TIMESTAMPTZ NOT NULL,
    fin_at TIMESTAMPTZ NOT NULL,
    modalidad VARCHAR(15) NOT NULL,
    ubicacion_id BIGINT REFERENCES ubicaciones(id) ON DELETE SET NULL,
    url_virtual TEXT,
    precio NUMERIC(10, 2) NOT NULL,
    moneda VARCHAR(3) NOT NULL,
    estado VARCHAR(20) NOT NULL DEFAULT 'PENDIENTE',
    expira_at TIMESTAMPTZ,
    notas_cliente TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cancelada_at TIMESTAMPTZ,
    CONSTRAINT chk_reservas_sesion_modalidad CHECK (modalidad IN ('PRESENCIAL', 'ONLINE', 'AMBAS')),
    CONSTRAINT chk_reservas_sesion_estado CHECK (estado IN ('PENDIENTE', 'CONFIRMADA', 'CANCELADA', 'LISTA_ESPERA', 'COMPLETADA', 'NO_ASISTIO')),
    CONSTRAINT chk_reservas_sesion_fechas CHECK (fin_at > inicio_at),
    CONSTRAINT chk_reservas_sesion_precio CHECK (precio >= 0)
);

CREATE TABLE historial_estados_reserva (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
