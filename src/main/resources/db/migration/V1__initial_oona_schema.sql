    reserva_id BIGINT NOT NULL,
    estado_anterior VARCHAR(20),
    estado_nuevo VARCHAR(20) NOT NULL,
    cambiado_por BIGINT REFERENCES usuarios(id) ON DELETE SET NULL,
    motivo VARCHAR(255),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_historial_tipo_reserva CHECK (tipo_reserva IN ('EVENTO', 'SESION'))
);

CREATE TABLE favoritos (
    cliente_id BIGINT NOT NULL REFERENCES perfil_clientes(id) ON DELETE CASCADE,
    tipo_entidad VARCHAR(15) NOT NULL,
    entidad_id BIGINT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
